
# declarando as variáveis "principais".
numero1 = 10
numero2 = 5

#declarando uma váriavel para armazanar o resultado da soma.
soma = numero1 + numero2

# imprimindo o resultado da soma.   
print(f"A soma dos números: {numero1} e {numero2} é:{soma}") 

#declarando uma váriavel para armazanar o resultado da subtração.
subtraçao = numero1 - numero2
print(f"O resultado da subtração entre {numero1} e {numero2} é: {subtraçao}")

#declarando uma váriavel para armazanar o resultado da multiplicação.
multiplicaçao = numero1 * numero2   
print(f"O resultado da multiplicação entre {numero1} e {numero2} é: {multiplicaçao}")

#declarando uma váriavel para armazanar o resultado da divisão.
divisao = numero1 / numero2 
print(f"O resultado da divisão entre {numero1} e {numero2} é: {divisao}")